

package model;


public class Device {
    private int maLK;
    private String tenLK;
    private String nsx;
    private int giaLK;
    
    //constructor
    public Device(){
    }
    
    //getter , setter

    public int getMaLK() {
        return maLK;
    }

    public void setMaLK(int maLK) {
        this.maLK = maLK;
    }

    public String getTenLK() {
        return tenLK;
    }

    public void setTenLK(String tenLK) {
        this.tenLK = tenLK;
    }

    public String getNsx() {
        return nsx;
    }

    public void setNsx(String nsx) {
        this.nsx = nsx;
    }

    public int getGiaLK() {
        return giaLK;
    }

    public void setGiaLK(int giaLK) {
        this.giaLK = giaLK;
    }
    
    
}

